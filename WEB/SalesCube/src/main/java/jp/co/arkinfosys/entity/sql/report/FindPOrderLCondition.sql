SELECT
	DISTINCT
	LINE.HIST_ID,
	LINE.ACTION_TYPE,
	LINE.ACTION_FUNC,
	LINE.REC_DATETM,
	LINE.REC_USER,
	LINE.PO_LINE_ID,
	LINE.STATUS,
	LINE.PO_SLIP_ID,
	LINE.LINE_NO,
	LINE.PRODUCT_CODE,
	LINE.SUPPLIER_PCODE,
	LINE.PRODUCT_ABSTRACT,
	LINE.PRODUCT_REMARKS,
	LINE.QUANTITY,
	LINE.TEMP_UNIT_PRICE_CATEGORY,
	LINE.TAX_CATEGORY,
	LINE.SUPPLIER_CM_CATEGORY,
	LINE.UNIT_PRICE,
	LINE.PRICE,
	LINE.CTAX_PRICE,
	LINE.CTAX_RATE,
	LINE.DOL_UNIT_PRICE,
	LINE.DOL_PRICE,
	LINE.RATE,
	LINE.DELIVERY_DATE,
	LINE.REMARKS,
	LINE.REST_QUANTITY,
	LINE.CRE_FUNC,
	LINE.CRE_DATETM,
	LINE.CRE_USER,
	LINE.UPD_FUNC,
	LINE.UPD_DATETM,
	LINE.UPD_USER
FROM
	PO_SLIP_TRN_HIST_/*$domainId*/ SLIP
	INNER JOIN PO_LINE_TRN_HIST_/*$domainId*/ LINE ON SLIP.PO_SLIP_ID = LINE.PO_SLIP_ID
/*BEGIN*/
WHERE
	/*IF recDateFrom != null */
	AND CAST(LINE.REC_DATETM AS DATE) >= CAST(/*recDateFrom*/'2010/01/01' AS DATE)
	/*END*/
	/*IF recDateTo != null */
	AND CAST(LINE.REC_DATETM AS DATE) <= CAST(/*recDateTo*/'2010/01/01' AS DATE)
	/*END*/
	/*IF supplierCodeFrom != null */
	AND SLIP.SUPPLIER_CODE >= /*supplierCodeFrom*/'S'
	/*END*/
	/*IF supplierCodeTo != null */
	AND SLIP.SUPPLIER_CODE <= /*supplierCodeTo*/'S'
	/*END*/
	/*IF productCodeFrom != null */
	AND LINE.PRODUCT_CODE >= /*productCodeFrom*/'S'
	/*END*/
	/*IF productCodeTo != null */
	AND LINE.PRODUCT_CODE <= /*productCodeTo*/'S'
	/*END*/
/*END*/
ORDER BY LINE.HIST_ID

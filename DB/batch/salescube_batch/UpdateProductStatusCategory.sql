-- SP_UPDATE_PRODUCT_STATUS_CATEGORY プロシージャ呼び出し

SET @STARTDT=now();

call SP_UPDATE_PRODUCT_STATUS_CATEGORY_XXXXX;

SELECT
        CASE WHEN IFNULL(MAX(STATUS_CODE),1)=0 THEN 'SUCCESS' ELSE 'FAILURE' END AS RESULT
FROM
        BATCH_LOG
WHERE
        DOMAIN_ID='XXXXX' AND
        FUNC_NAME = 'SP_UPDATE_PRODUCT_STATUS_CATEGORY' AND
        DATETM >= @STARTDT;

quit
